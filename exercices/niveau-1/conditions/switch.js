// Condition switch - Vérifier le jour de la semaine, afficher la réponse dans la console
const day = "jeudi";

switch (day) {
  case "lundi":
    console.log("lundi");
    break;
  case "mardi":
    console.log("mardi");
    break;
  case "mercredi":
    console.log("mercredi");
    break;
  case "jeudi":
    console.log("jeudi");
    break;
  case "vendredi":
    console.log("vendredi");
    break;
  case "samedi":
    console.log("samedi");
    break;
  case "dimanche":
    console.log("dimanche");
    break;
  default:
    console.log("erreur");
}
